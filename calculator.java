public class calculator {

	static int add(int a, int b){ //Antony
		// Devuelve la suma entera de 2 numeros enteros recibidos
		return a + b;
	}
	static int sub(int a, int b){  //Oliver 
		//Devuelve la resta 2 numeros enteros
		return a - b;
	}
	
	static int mul(int a, int b){ //Jessica
		//Devuelve el producto de 2 números enteros
		return a * b;
	}
	
	static int div(int a, int b){  //Josue
		//Devuelve la división de 2 números enteros
		int result;
		result = a / b;
		return result;
	}
	
	static int mod(int a, int b){  //Carlos
		//Devuelve el módulo de 2 números enteros
		return a % b;
	}

}
